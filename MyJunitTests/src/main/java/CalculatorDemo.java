/**
 * Calculadora para ejercicio de Testing
 *
 * @author German Vazquez
 */
public class CalculatorDemo {

    /**
     * Método de SUMA de 2 valores tipo 'double'
     *
     * @param a double, primer parametro
     * @param b double, segundo parametro
     * @author German Vazquez
     */
    public double add(double a, double b) {
        return a + b;
    }

    /**
     * Método de MULTIPLICACIÓN de 2 valores 'double'
     *
     * @param a double, primer parametro
     * @param b double, segundo parametro
     * @author German Vazquez
     */
    public double times(double a, double b) {
        return a * b;
    }

}
